<h2>Technical information about this website&rsquo;s accessibility</h2>

<p>The University of Edinburgh is committed to making its websites and applications accessible, in accordance with the Public Sector Bodies (Websites and Mobile Applications) (No.&nbsp;2) Accessibility Regulations 2018.</p>

<h2>Compliance Status</h2>

<p>This website is partially compliant with the Web Content Accessibility Guidelines (WCAG) 2.2 AA standard, due to the non-compliances listed below.</p>

<p>The full guidelines are available at:</p>

<p><a href="https://www.w3.org/TR/WCAG22/">Web Content Accessibility Guidelines (WCAG) 2.2 AA standard</a></p>

<h2>Non accessible content</h2>

<p>The content listed below is non-accessible for the following reasons.</p>

<h3>Noncompliance with the accessibility regulations</h3>

<p>The following items do not comply with the WCAG 2.2 AA success criteria:</p>

<ul>
    <li>Not all non-text items have alternative text
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#non-text-content">1.1.1 Non-text Content</a></li>
        </ul>
    </li>
    <li>Not all video content has a transcript, human-corrected captions or audio description
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#audio-only-and-video-only-prerecorded">1.2.1 Audio-only and Video-only (Prerecorded)</a></li>
            <li><a href="https://www.w3.org/TR/WCAG22/#captions-prerecorded">1.2.2 Captions (Prerecorded)</a></li>
            <li><a href="https://www.w3.org/TR/WCAG22/#audio-description-prerecorded">1.2.5 Audio Description (Prerecorded)</a></li>
        </ul>
    </li>
    <li>Some actionable hyperlinks are shown by colour only
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#use-of-color">1.4.1 Use of Colour</a></li>
        </ul>
    </li>
    <li>There may not be sufficient contrast between font and background colour, especially on buttons, menus and tables
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#contrast-minimum">1.4.3 Contrast (Minimum)</a></li>
        </ul>
    </li>
    <li>Not all text can be scaled effectively up to 200% without distortion
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#resize-text">1.4.4 Resize Text</a></li>
        </ul>
    </li>
    <li>When the browser zoom is used to scale content to 400%, it does not reflow
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#reflow">1.4.10 Reflow</a></li>
        </ul>
    </li>
    <li>There is no &lsquo;Skip to main content&rsquo; option to avoid sections such as headers or navigation links that are repeated on multiple pages
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#bypass-blocks">2.4.1 Bypass Blocks</a></li>
        </ul>
    </li>
    <li>Not all links are formatted correctly as meaningful hypertext
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#link-purpose-in-context">2.4.4 Link Purpose (In Context)</a></li>
        </ul>
    </li>
    <li>There are missing headings and labels which can hinder finding and navigating content
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#headings-and-labels">2.4.6 Headings and Labels</a></li>
        </ul>
    </li>
    <li>The keyboard focus indicator is not always clearly discernible due to poor colour contrast and weight
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#focus-visible">2.4.7 Focus Visible</a></li>
        </ul>
    </li>
    <li>Some links open in a new tab or window without warning
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#on-input">3.2.2 On Input</a></li>
        </ul>
    </li>
    <li>The contact email link does not appear on every page in the same place
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#consistent-help">3.2.6 Consistent Help</a></li>
        </ul>
    </li>
</ul>

