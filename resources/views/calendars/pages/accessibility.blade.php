<p><a href="https://www.ed.ac.uk/student-disability-service/staff/supporting-students/accessible-technology">Information on SensusAccess</a></p>

<h2>How accessible this website is</h2>

<p>We know some parts of this website are not fully accessible:</p>

<ul>
    <li>Some colours on our pages (particularly buttons, menus and tables) do not meet current colour contrast recommendations</li>
    <li>The site is not fully compatible with browser customisation, which means some user control and functionality is lost</li>
    <li>There is no reflow to 400% magnification so users have to scroll horizontally as well as vertically</li>
    <li>Some non-text items have missing alt text descriptions</li>
    <li>Not all the controls on video content are focusable</li>
    <li>Videos do not display on Android mobile devices</li>
    <li>Not all audio-visual content has transcripts, human-corrected captions or audio descriptions</li>
    <li>Some links are not formatted correctly as meaningful hypertext</li>
    <li>Some information is conveyed by colour alone</li>
    <li>Some links open in a new tab or window without warning</li>
    <li>There is no &lsquo;Skip to main content&rsquo; button</li>
    <li>There are missing headings and labels which affects navigation with screen reading software</li>
    <li>It can be hard to tell where you have navigated to using the keyboard</li>
    <li>The contact email link does not appear in a consistent position or on every page</li>
</ul>

