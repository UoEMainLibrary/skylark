<h1>Accessibility statement for <a href="https://collections.ed.ac.uk/calendars">University Calendars</a></h1>

<p>Website accessibility statement in line with Public Sector Body (Websites and Mobile Applications) (No.&nbsp;2) Accessibility Regulations 2018</p>

<p>This accessibility statement applies to:</p>
<p><a href="https://collections.ed.ac.uk/calendars">https://collections.ed.ac.uk/calendars</a></p>

<p>This website is hosted by the University of Edinburgh Library on behalf of the University of Edinburgh. It holds the University Calendars which are produced every year to showcase the diverse range of material held in the University's collections. We want as many people as possible to be able to use this application. For example, that means you should be able to:</p>

<ul>
    <li>Access the site without a cognitive function test</li>
    <li>Change the majority of colours and contrast level</li>
    <li>Magnify the site to 500%</li>
    <li>Experience no time limits to accessing content</li>
    <li>Experience no flashing, scrolling or moving content</li>
    <li>Interact with site content without the need for dragging movements</li>
    <li>Navigate most of the website using just a keyboard</li>
    <li>Navigate most of the website using speech recognition software</li>
    <li>Listen to most of the website using a screen reader (including the most recent versions of JAWS, TalkBack and VoiceOver)</li>
</ul>

<p>We&rsquo;ve also made the website text as simple as possible to understand. However, some of our content is technical, and we use technical terms where there is no easier wording we could use without changing what the text means.</p>

<h2>Customising the website</h2>

<p>AbilityNet has advice on making your device easier to use if you have a disability. This is an external site with suggestions to make your computer more accessible:</p>

<p><a href="https://mcmw.abilitynet.org.uk/">AbilityNet - My Computer My Way</a></p>

<p>With a few simple steps you can customise the appearance of our website using your browser settings to make it easier to read and navigate:</p>

<p><a href="https://www.ed.ac.uk/about/website/accessibility/customising-site">Additional information on how to customise our website appearance</a></p>

