<h2>Technical information about this website&rsquo;s accessibility</h2>

<p>The University of Edinburgh is committed to making its websites and applications accessible, in accordance with the Public Sector Bodies (Websites and Mobile Applications) (No.&nbsp;2) Accessibility Regulations 2018.</p>

<h2>Compliance Status</h2>

<p>This website is partially compliant with the Web Content Accessibility Guidelines (WCAG) 2.2 AA standard, due to the non-compliances listed below.</p>

<p>The full guidelines are available at:</p>

<p><a href="https://www.w3.org/TR/WCAG22/">Web Content Accessibility Guidelines (WCAG) 2.2 AA standard</a></p>

<h2>Non accessible content</h2>

<p>The content listed below is non-accessible for the following reasons.</p>

<h3>Noncompliance with the accessibility regulations</h3>

<p>The following items do not comply with the WCAG 2.2 AA success criteria:</p>

<ul>
    <li>Not all images and logos possess meaningful or descriptive alternative text
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#non-text-content">1.1.1 &ndash; Non-text Content</a></li>
        </ul>
    </li>
    <li>Some information is conveyed by colour only e.g. links
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#use-of-color">1.4.1 &ndash; Use of Colour</a></li>
        </ul>
    </li>
    <li>Not all colour contrasts meet minimum accessibility standards
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#contrast-minimum">1.4.3 &ndash; Contrast (Minimum)</a></li>
        </ul>
    </li>
    <li>The website cannot be viewed reliably beyond 150% magnification without overlapping content and loss of functionality
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#resize-text">1.4.4 &ndash; Resize Text</a></li>
        </ul>
    </li>
    <li>Site is not fully compatible with browser customisation meaning that users do not have full control and functionality when customising the site
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#visual-presentation">1.4.8 &ndash; Visual Presentation (AAA)</a></li>
        </ul>
    </li>
    <li>Content overlaps beyond 200% and the page does not reflow up to 400% without horizontal scrolling
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#reflow">1.4.10 &ndash; Reflow</a></li>
        </ul>
    </li>
    <li>Some tooltips are not accessible via keyboard or assistive technologies
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#content-on-hover-or-focus">1.4.13 &ndash; Content on Hover or Focus</a></li>
        </ul>
    </li>
    <li>The moving carousel on the homepage cannot be paused or stopped by the user
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#pause-stop-hide">2.2.2 &ndash; Pause, Stop, Hide</a></li>
        </ul>
    </li>
    <li>Several naked URLs are present, and not all links are formatted appropriately with meaningful hypertext
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#link-purpose-in-context">2.4.4 &ndash; Link Purpose (In Context)</a></li>
        </ul>
    </li>
    <li>Keyboard focus visibility is insufficient in some browsers (particularly Firefox), making it difficult to determine where the user has navigated to
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#focus-visible">2.4.7 &ndash; Focus Visible</a></li>
        </ul>
    </li>
    <li>Some site elements and overlays obscure other content at higher magnification levels, particularly above 150%
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#focus-not-obscured-minimum">2.4.11 &ndash; Focus Not Obscured (Minimum)</a></li>
        </ul>
    </li>
    <li>Error messages could be more descriptive and informative
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#error-identification">3.3.1 &ndash; Error Identification</a></li>
        </ul>
    </li>
    <li>There are missing labels present in the website that fail to describe the purpose of the input form
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#labels-or-instructions">3.3.2 &ndash; Labels or Instructions</a></li>
        </ul>
    </li>
    <li>Content is not robust enough that it can be interpreted by a wide variety of user agents, including assistive technologies e.g. some interactive elements, including buttons, do not have discernible text or programmatic labels
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#name-role-value">4.1.2 &ndash; Name, Role, Value</a></li>
        </ul>
    </li>
</ul>

<p>At this time, we believe all items are within our control. Unless specified otherwise, a complete solution, or significant improvement, will be in place for those items within our control by November 2026.</p>

<h3>Disproportionate burden</h3>

