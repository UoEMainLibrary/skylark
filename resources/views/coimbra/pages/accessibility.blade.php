<h3>Content that&rsquo;s not within the scope of the accessibility regulations</h3>

<h4>Interactive maps</h4>

<p>This website hosts a mapping service. Currently, online mapping and mapping services sit outwith the 2018 accessibility regulations.</p>

<div style="padding-left:2rem;">
    <p>&ldquo;These Regulations do not apply to the following content of a website or mobile application of a public sector body&mdash;</p>
    <p>(d) online maps and mapping services, as long as essential information is provided in an accessible digital manner for maps intended for navigational use;&rdquo; (The Public Sector Bodies (Websites and Mobile Applications) (No.&nbsp;2) Accessibility Regulations 2018)</p>
</div>

<p>The mapping service is not fully accessible, as it is not possible to interact with individual map features using assistive technologies. Although maps are currently not within the scope of the regulations, we will continue to strive to make the site as accessible as possible.</p>

<p>There is additional information available in different formats, including maps, to download and information in the AccessAble - UoE app available for iOS and Android:</p>

<p><a href="https://www.accessable.co.uk">AccessAble Information</a></p>

<h2>What we&rsquo;re doing to improve accessibility</h2>

<p>We will continue to address and make significant improvements to the accessibility issues highlighted. Unless specified otherwise, a complete solution or significant improvement will be in place by November 2026. At this time we believe the site is fully under our control.</p>

<p>While we are in the process of resolving these accessibility issues we will ensure reasonable adjustments are in place to make sure no user is disadvantaged. As changes are made, we will continue to review accessibility and retest the accessibility of this website.</p>

<h2>Preparation of this accessibility statement</h2>

<p><strong>This statement was prepared on 7th August 2020. It was last reviewed on 27th October 2025.</strong></p>

<p><strong>The website was last tested on 2nd October 2025.</strong> The testing was carried out by Library and University Collections, Information Services Group at the University of Edinburgh using both automated and manual methods. The site was tested on a PC, primarily using Microsoft Edge alongside Mozilla Firefox and Google Chrome.</p>

<p>Recent world-wide usage levels survey for different screen readers and browsers shows that Chrome, Mozilla Firefox and Microsoft Edge are increasing in popularity and Google Chrome is now the favoured browser for screen readers:</p>

<p><a href="https://webaim.org/projects/screenreadersurvey9/">WebAIM: Screen Reader User Survey</a></p>

<p>The aforementioned three browsers have been used in certain questions for reasons of breadth and variety.</p>

<p>We ran automated testing using <a href="https://www.deque.com/axe/devtools/chrome-browser-extension/">AXE Devtools</a> and then manual testing that included:</p>

<ul>
    <li>Spell check functionality;</li>
    <li>Scaling using different resolutions and reflow;</li>
    <li>Options to customise the interface (magnification, font, background colour, etc);</li>
    <li>Keyboard navigation and keyboard traps;</li>
    <li>Data validation;</li>
    <li>Warning of links opening in new tab or window;</li>
    <li>Information conveyed in the colour or sound only;</li>
    <li>Flashing, moving or scrolling text;</li>
    <li>Use with screen reading software (for example JAWS);</li>
    <li>Assistive software (TextHelp Read and Write, Windows Magnifier, ZoomText, Dragon Naturally Speaking, TalkBack and VoiceOver);</li>
    <li>Tooltips and text alternatives for any non-text content;</li>
    <li>Time limits;</li>
    <li>Compatibility with mobile accessibility functionality (Android and iOS);</li>
    <li>Any drag functionality and alternatives;</li>
    <li>Consistent help function;</li>
    <li>Submission and re-entry of data;</li>
    <li>Any cognitive tests.</li>
</ul>

