        <h2>Technical information about this website's accessibility</h2>
        <p>The University of Edinburgh is committed to making its websites and applications accessible, in accordance with the Public Sector Bodies (Websites and Mobile Applications) (No. 2) Accessibility Regulations 2018.</p>

        <h2>Compliance Status</h2>
        <p>This website is partially compliant with the Web Content Accessibility Guidelines (WCAG) 2.2 AA standard, due to the non-compliances listed below.</p>
        <p>The full guidelines are available at:</p>
        <p><a href="https://www.w3.org/TR/WCAG22/" target="_blank" rel="noopener noreferrer">Web Content Accessibility Guidelines (WCAG) 2.2 AA standard<span class="visually-hidden"> (opens in a new tab)</span></a></p>

        <h2>Non accessible content</h2>
        <p>The content listed below is non-accessible for the following reasons.</p>

        <h3>Noncompliance with the accessibility regulations</h3>

        <ul><li>Content overlap occurs in both portrait and landscape views on mobile devices</li></ul>
        <p><a href="https://www.w3.org/TR/WCAG22/#orientation" target="_blank" rel="noopener noreferrer">1.3.4 - Orientation<span class="visually-hidden"> (opens in a new tab)</span></a></p>

        <ul><li>Some links are not underlined and rely on colour alone</li></ul>
        <p><a href="https://www.w3.org/TR/WCAG22/#use-of-color" target="_blank" rel="noopener noreferrer">1.4.1 - Use of Colour<span class="visually-hidden"> (opens in a new tab)</span></a></p>

        <ul><li>There are instances of text presented as images</li></ul>
        <p><a href="https://www.w3.org/TR/WCAG22/#images-of-text" target="_blank" rel="noopener noreferrer">1.4.5 - Images of Text<span class="visually-hidden"> (opens in a new tab)</span></a></p>

        <ul><li>Pages do not reflow when zooming beyond 200%</li></ul>
        <p><a href="https://www.w3.org/TR/WCAG22/#reflow" target="_blank" rel="noopener noreferrer">1.4.10 - Reflow<span class="visually-hidden"> (opens in a new tab)</span></a></p>

        <ul><li>Tabbing order in the navigation menu is illogical and does not follow a consistent left-to-right reading order</li></ul>
        <p><a href="https://www.w3.org/TR/WCAG22/#meaningful-sequence" target="_blank" rel="noopener noreferrer">1.3.2 - Meaningful Sequence<span class="visually-hidden"> (opens in a new tab)</span></a></p>
        <p><a href="https://www.w3.org/TR/WCAG22/#focus-order" target="_blank" rel="noopener noreferrer">2.4.3 - Focus Order<span class="visually-hidden"> (opens in a new tab)</span></a></p>

        <ul><li>Several links throughout the site are not formatted with meaningful hypertext</li></ul>
        <p><a href="https://www.w3.org/TR/WCAG22/#link-purpose-in-context" target="_blank" rel="noopener noreferrer">2.4.4 - Link Purpose (In Context)<span class="visually-hidden"> (opens in a new tab)</span></a></p>

        <ul><li>Content is not robust enough that it can be interpreted by a wide variety of user agents, including assistive technologies e.g. some form elements do not have labels and some select elements do not have accessible names</li></ul>
        <p><a href="https://www.w3.org/TR/WCAG22/#name-role-value" target="_blank" rel="noopener noreferrer">4.1.2 - Name, Role, Value<span class="visually-hidden"> (opens in a new tab)</span></a></p>

        <ul><li>There are PDFs that are not fully accessible</li></ul>
        <p><a href="https://www.w3.org/TR/WCAG22/#non-text-content" target="_blank" rel="noopener noreferrer">1.1.1 - Non-text Content<span class="visually-hidden"> (opens in a new tab)</span></a></p>
        <p><a href="https://www.w3.org/TR/WCAG22/#keyboard" target="_blank" rel="noopener noreferrer">2.1.1 - Keyboard<span class="visually-hidden"> (opens in a new tab)</span></a></p>
        <p><a href="https://www.w3.org/TR/WCAG22/#name-role-value" target="_blank" rel="noopener noreferrer">4.1.2 - Name, Role, Value<span class="visually-hidden"> (opens in a new tab)</span></a></p>

        <p>We aim to improve our websites accessibility on a regular and continuous basis. See the section below ('What we're doing to improve accessibility') on how we are improving our site accessibility.</p>

        <p>We are working towards solving these problems and expect significant improvements by June 2026. The site is fully within our control.</p>

        <h3>Disproportionate burden</h3>
