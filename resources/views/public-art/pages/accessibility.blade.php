<h2>Technical information about this website&rsquo;s accessibility</h2>

<p>The University of Edinburgh is committed to making its websites and applications accessible, in accordance with the Public Sector Bodies (Websites and Mobile Applications) (No.&nbsp;2) Accessibility Regulations 2018.</p>

<h2>Compliance Status</h2>

<p>This website is partially compliant with the Web Content Accessibility Guidelines (WCAG) 2.2 AA standard, due to the non-compliances listed below.</p>

<p>The full guidelines are available at:</p>

<p><a href="https://www.w3.org/TR/WCAG22/">Web Content Accessibility Guidelines (WCAG) 2.2 AA standard</a></p>

<h2>Non accessible content</h2>

<p>The content listed below is non-accessible for the following reasons.</p>

<h3>Noncompliance with the accessibility regulations</h3>

<p>The following items to not comply with the WCAG 2.2 AA success criteria:</p>

<ul><li>Not all non-text content presented to users has alternative text.</li></ul>
<p><a href="https://www.w3.org/TR/WCAG22/#non-text-content">1.1.1 - Non-text Content</a></p>

<ul><li>Not all video holds captions, subtitles that identify all speakers, or note other significant sounds (e.g. laughter)</li></ul>
<p><a href="https://www.w3.org/TR/WCAG22/#captions-prerecorded">1.2.2 - Captions (Prerecorded)</a></p>

<ul><li>Information, structure and relationships conveyed through presentation cannot always be programmatically determined. This includes missing heading tags.</li></ul>
<p><a href="https://www.w3.org/TR/WCAG22/#info-and-relationships">1.3.1 - Info and Relationships</a></p>
<p><a href="https://www.w3.org/TR/WCAG22/#headings-and-labels">2.4.6 - Headings and Labels</a></p>

<ul><li>Content will only display in a single page orientation.</li></ul>
<p><a href="https://www.w3.org/TR/WCAG22/#orientation">1.3.4 - Orientation</a></p>

<ul><li>Colour is used as the only visual means of conveying information, indicating an action, prompting a response, or distinguishing a visual element</li></ul>
<p><a href="https://www.w3.org/TR/WCAG22/#use-of-color">1.4.1 - Use of Color</a></p>

<ul><li>There may not be sufficient colour contrast between font and background colours, especially where the text size is small.</li></ul>
<p><a href="https://www.w3.org/TR/WCAG22/#contrast-minimum">1.4.3 - Contrast (Minimum)</a></p>

<ul><li>Text cannot be resized without assistive technology up to 200 percent without loss of content or functionality.</li></ul>
<p><a href="https://www.w3.org/TR/WCAG22/#resize-text">1.4.4. Resize Text</a></p>

<ul><li>Information is conveyed as an image of text rather than as text itself, making it incompatible with screen readers and other assistive technology.</li></ul>
<p><a href="https://www.w3.org/TR/WCAG22/#images-of-text">1.4.5 - Images of text</a></p>

<ul><li>Site is not fully compatible with browser customisation meaning that users do not have full control and functionality when customising the site (WCAG 2.1 AAA).</li></ul>
<p><a href="https://www.w3.org/TR/WCAG22/#visual-presentation">1.4.8 - Visual Presentation</a></p>

<ul><li>Content cannot be presented without loss of information or functionality, requiring the user to scroll in two dimensions.</li></ul>
<p><a href="https://www.w3.org/TR/WCAG22/#reflow">1.4.10 - Reflow</a></p>

<ul><li>It is not possible to use a keyboard to access all the content.</li></ul>
<p><a href="https://www.w3.org/TR/WCAG22/#keyboard">2.1.1 - Keyboard</a></p>

<ul><li>Users are not always able to pause, stop or hide moving items that appear at some points on the webpage.</li></ul>
<p><a href="https://www.w3.org/TR/WCAG22/#pause-stop-hide">2.2.2 - Pause, Stop, Hide</a></p>

<ul><li>There is no &lsquo;skip to main content&rsquo; option available throughout the website.</li></ul>
<p><a href="https://www.w3.org/TR/WCAG22/#bypass-blocks">2.4.1 - Bypass Blocks</a></p>

<ul><li>Some of our page titles do not fully describe the page content and some pages are missing titles.</li></ul>
<p><a href="https://www.w3.org/TR/WCAG22/#page-titled">2.4.2 &ndash; Page Titled</a></p>

<ul><li>The purpose of each link cannot be determined from the text alone.</li></ul>
<p><a href="https://www.w3.org/TR/WCAG22/#link-purpose-in-context">2.4.4 - Link Purpose (In Context)</a></p>

<ul><li>Information, structure and relationships conveyed through presentation cannot always be programmatically determined. This includes a missing &lt;h1&gt; on the homepage.</li></ul>
<p><a href="https://www.w3.org/TR/WCAG22/#headings-and-labels">2.4.6 &ndash; Headings and Labels</a></p>

<ul><li>Visual information to identify user interface components, such as keyboard focus, do not always have a sufficient contrast ratio.</li></ul>
<p><a href="https://www.w3.org/TR/WCAG22/#focus-visible">2.4.7 - Focus Visible</a></p>

<ul><li>Some links open in a new browser window without warning.</li></ul>
<p><a href="https://www.w3.org/TR/WCAG22/#on-input">3.2.2 - On Input</a></p>

<ul><li>There are missing labels present in the website that fail to describe the purpose of the input form</li></ul>
<p><a href="https://www.w3.org/TR/WCAG22/#labels-or-instructions">3.3.2 - Labels or Instructions</a></p>

<ul><li>Screen readers and voice recognition software are not able to identify some parts of the page.</li></ul>
<p><a href="https://www.w3.org/TR/WCAG22/#name-role-value">4.1.2 - Name, Role, Value</a></p>

<p>At this time, we believe all items are within our control. Unless specified otherwise, a complete solution, or significant improvement, will be in place for those items within our control by July 2027.</p>

<h3>Disproportionate burden</h3>

<p>We are not currently claiming that any accessibility problems would be a disproportionate burden to fix.</p>

<h3>Content that&rsquo;s not within the scope of the accessibility regulations</h3>

<p><b>Interactive maps</b></p>

<p>This website hosts a mapping service. Currently, online mapping and mapping services sit outwith the 2018 accessibility regulations.</p>

<p>&ldquo;These Regulations do not apply to the following content of a website or mobile application of a public sector body&mdash;</p>

<p>(d)online maps and mapping services, as long as essential information is provided in an accessible digital manner for maps intended for navigational use;&rdquo; (The Public Sector Bodies (Websites and Mobile Applications) (No. 2) Accessibility Regulations 2018)&rdquo;</p>

<p>The mapping service is not fully accessible, as it is not possible to interact with individual map features using assistive technologies. Although maps are currently not within the scope of the regulations, we will continue to strive to make the site as accessible as possible.</p>

<p>There is additional information available in different formats, including maps, to download and information in the AccessAble - UoE app available for iOS and Android:</p>

<p><a href="https://www.ed.ac.uk/estates/buildings-information/disability">AccessAble Information</a>.</p>

<h2>What we&rsquo;re doing to improve accessibility</h2>

<p>At this time, we believe all items are within our control. We will continue to address the accessibility issues highlighted to deliver a solution or suitable workaround. We are looking to move this site to a new content management system within the next 12 months and will be working to ensure this resolves the accessibility issues. Unless specified otherwise, a complete solution or significant improvement will be in place for those items within our control by July 2027.</p>

<p>While we are in the process of resolving these accessibility issues, or where we are unable, we will ensure reasonable adjustments are in place to make sure no user is disadvantaged. As changes are made, we will continue to review accessibility and retest the accessibility of this website.</p>

<h2>Preparation of this accessibility statement</h2>

<p><b>This statement was first prepared on 7th August 2020. It was last reviewed on 07th July 2026.</b></p>

<p>This website was first tested on 7th August 2020, reviewed on 15th September 2021 and then again on 7th October 2022. Further testing is underway for completion by end July 2026 and this statement will be updated on this basis. The testing was carried out by The University of Edinburgh Library and University Collections Digital Library Development team The August 2020 and September 2021 only consisted of automated testing <a href="https://wave.webaim.org/">WAVE WebAIM</a> and <a href="https://littleforest.co.uk/">Little Forest</a>. The testing in October 2022 included full manual as well as automated testing.</p>

<p>This website was last manually tested by the Digital Library team, Library and University Collections, University of Edinburgh in September 2022 following on from automated testing of the system the previous two years. This was primarily using Mozilla Firefox (91.7.1esr), Microsoft Edge (99.0.1150.55), Google Chrome (99.0.4844.84), and Internet Explorer (IE) (19043.1586) browsers for comparative purposes.</p>

<p>Recent world-wide usage levels survey for different screen readers and browsers shows that Chrome, Mozilla Firefox and Microsoft Edge are increasing in popularity and Google Chrome is now the favoured browser for screen readers:</p>

<p><a href="https://webaim.org/projects/screenreadersurvey10/">WebAIM: Screen Reader User Survey</a></p>

<p>The aforementioned three browsers have been used in certain questions for reasons of breadth and variety.</p>

<p>We ran automated testing using <a href="https://wave.webaim.org/">WAVE WebAIM</a> and <a href="https://littleforest.co.uk/">Little Forest</a>, then manual testing that included:</p>

<ul>
    <li>Spell check functionality;</li>
    <li>Scaling using different resolutions and reflow;</li>
    <li>Options to customise the interface (magnification, font, background colour, etc);</li>
    <li>Keyboard navigation and keyboard traps;</li>
    <li>Data validation;</li>
    <li>Warning of links opening in new tab or window;</li>
    <li>Information conveyed in the colour or sound only;</li>
    <li>Flashing, moving or scrolling text;</li>
    <li>Operability if JavaScript is disabled;</li>
    <li>Use with screen reading software (for example JAWS);</li>
    <li>Assistive software (TextHelp Read and Write, Windows Magnifier, ZoomText, Dragon Naturally Speaking, TalkBack and VoiceOver);</li>
    <li>Tooltips and text alternatives for any non-text content;</li>
    <li>Time limits;</li>
    <li>Compatibility with mobile accessibility functionality (Android and iOS);</li>
</ul>

<h2>Change Log</h2>

<p>Since our first evaluation and statement, which was based on automated testing, we have undertaken extensive manual testing. This includes utilising a range of assistive technology to ensure we have a clear picture of the accessibility issues and how best to resolve them.</p>
