            <h1>Accessibility statement for Musical Instruments Museums Edinburgh website</h1>

            <p>Website accessibility statement inline with Public Sector Body (Websites and
                Mobile Applications) (No. 2) Accessibility Regulations 2018</p>

            <p>This accessibility statement applies to:
                <a href="https://collections.ed.ac.uk/mimed">https://collections.ed.ac.uk/mimed</a></p>

            <p>This website is run by the Library and University Collections Directorate which
                is part of Information Services Group at the University of Edinburgh. We want as many people as possible to be able to use this application. For
                example, that means you should be able to:</p>
            <ul>
                <li>using your browser settings, change most colours, contrast levels and fonts;</li>
                <li>You can magnify content up to 200%;</li>
                <li>use the website without encountering any time limits;</li>
                <li>use the website without needing to drag any content using a mouse only.</li>
                <li>It is clear where you have keyboard navigated too</li>
            </ul>

            <p>We've also made the website text as simple as possible to understand.
                However, some of our content is technical, and we use technical terms where there is no easier wording
                we could use without changing what the text means.</p>

            <h2>Customising the website</h2>
            <p>AbilityNet has advice on making your device easier to use if you have a disability. This is an external site with suggestions to make your computer more accessible:</p>
            <p><a href="https://mcmw.abilitynet.org.uk/">AbilityNet - My Computer My Way</a></p>
            <p>With a few simple steps you can customise the appearance of our website using your browser settings to make it easier to read and navigate:</p>
            <p><a href="https://www.ed.ac.uk/about/website/accessibility/customising-site" title="Customising our site">Additional information on how to customise our website appearance</a></p>
            <p>If you are a member of University staff or a student, you can use the free SensusAccess accessible document conversion service:</p>
            <p><a href="https://www.ed.ac.uk/student-disability-service/staff/supporting-students/accessible-technology">Information on SensusAccess</a></p>

            <h2>How accessible this website is</h2>
            <p>We know some parts of this website are not fully accessible:</p>
            <ul>
                <li>there are images of text</li>
                <li>all images do not have alt text</li>
                <li>some hyperlinks are conveyed with colour only</li>
                <li>some hyperlinks are not formatted correctly as meaningful hypertext</li>
                <li>not all non-text content presented to users has alternative text</li>
                <li>it is not possible to access all content by using the keyboard alone</li>
                <li>keyboard navigation does not follow logical ordering</li>
                <li>not all icons have tooltips and these are available via mouse hover but not when using the keyboard or assistive software</li>
                <li>Reflow is not enabled on the site, up to 400% magnification</li>
                <li>some parts of the site may not be fully compatible with screen readers such as JAWS or NVDA</li>
                <li>Some parts of the site are not fully compatible with voice recognition software such as Dragon</li>
                <li>Audio content is missing a text alternative</li>
                <li>Video content is missing captions and audio description</li>
                <li>Headings are not used properly on the site as some heading levels are missing or skipped</li>
            </ul>

