<h2>Technical information about this website&rsquo;s accessibility</h2>

<p>The University of Edinburgh is committed to making its websites and applications accessible, in accordance with the Public Sector Bodies (Websites and Mobile Applications) (No.&nbsp;2) Accessibility Regulations 2018.</p>

<h2>Compliance Status</h2>

<p>This website is partially compliant with the Web Content Accessibility Guidelines (WCAG) 2.2 AA standard, due to the non-compliances listed below.</p>

<p>The full guidelines are available at:</p>

<p><a href="https://www.w3.org/TR/WCAG22/">Web Content Accessibility Guidelines (WCAG) 2.2 AA standard</a></p>

<h2>Non accessible content</h2>

<p>The content listed below is non-accessible for the following reasons.</p>

<h3>Noncompliance with the accessibility regulations</h3>

<p>The following items to not comply with the WCAG 2.2 AA success criteria:</p>

<ul>
    <li>Not all non-text items have alternate text
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#non-text-content">1.1.1 - Non Text Content</a></li>
        </ul>
    </li>
    <li>Audio content does not have transcripts/text alternative
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#audio-only-and-video-only-prerecorded">1.2.1 &ndash; Audio-only and Video-only (Prerecorded)</a></li>
        </ul>
    </li>
    <li>Audio and video content does not have captions
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#captions-prerecorded">1.2.2 - Captions (Prerecorded)</a></li>
        </ul>
    </li>
    <li>Video content does not have audio description
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#audio-description-or-media-alternative-prerecorded">1.2.3 - Audio Description or Media Alternative (Prerecorded)</a></li>
        </ul>
    </li>
    <li>Information, structure and relationships conveyed through presentation cannot always be programmatically determined. This includes a missing &lt;h1&gt; on the homepage.
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#info-and-relationships">1.3.1 - Info and Relationships</a></li>
        </ul>
    </li>
    <li>Keyboard navigation does not follow a logical order
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#meaningful-sequence">1.3.2 - Meaningful Sequence</a></li>
        </ul>
    </li>
    <li>Some hyperlinks are conveyed with colour only
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#use-of-color">1.4.1 - Use of Colour</a></li>
        </ul>
    </li>
    <li>Information is conveyed as an image-of-text rather than as text itself, making it not compatible with screen readers and other assistive technology
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#images-of-text">1.4.5 - Images of Text</a></li>
        </ul>
    </li>
    <li>Reflow is not enabled up to 400%
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#reflow">1.4.10 - Reflow</a></li>
        </ul>
    </li>
    <li>Tooltips cannot be activated by keyboard or assistive software
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#content-on-hover-or-focus">1.4.13 - Content on Hover or Focus</a></li>
        </ul>
    </li>
    <li>Not all elements of the site are navigable with keyboard
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#keyboard">2.1.1 - Keyboard</a></li>
        </ul>
    </li>
    <li>Keyboard navigation does not follow a logical ordering
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#focus-order">2.4.3 - Focus Order</a></li>
        </ul>
    </li>
    <li>There are some links that are not meaningful hypertext (e.g. plain URLs)
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#link-purpose-in-context">2.4.4 - Link Purpose (In Context)</a></li>
        </ul>
    </li>
    <li>Headings are not coded correctly, including missed heading levels
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#headings-and-labels">2.4.6 - Headings and Labels</a></li>
        </ul>
    </li>
    <li>Pop ups open without warning
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#on-input">3.2.2 On Input</a></li>
        </ul>
    </li>
    <li>The website is not fully compatible with assistive software as not all items are coded correctly e.g. &ndash; Select elements do not have accessible names, Form elements do not have labels
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG22/#name-role-value">4.1.2 - Name, Role, Value</a></li>
        </ul>
    </li>
</ul>

