        <h2>Non accessible content</h2>
        <p>The content listed below is non-accessible for the following reasons.</p>

        <h3>Noncompliance with the accessibility regulations</h3>
        <p>The following items do not comply with the WCAG 2.2 AA success criteria:</p>

        <ul><li>Not all non-text items have sufficient alternative text</li></ul>
        <p><a href="https://www.w3.org/TR/WCAG22/#non-text-content" target="_blank" rel="noopener">1.1.1 &ndash; Non&#8209;text Content</a></p>

        <ul><li>Some content is lost when viewing in portrait mode on mobile devices</li></ul>
        <p><a href="https://www.w3.org/TR/WCAG22/#orientation" target="_blank" rel="noopener">1.3.3 &ndash; Orientation</a></p>

        <ul><li>Some links are conveyed by colour alone</li></ul>
        <p><a href="https://www.w3.org/TR/WCAG22/#use-of-color" target="_blank" rel="noopener">1.4.1 &ndash; Use of Color</a></p>

        <ul><li>Insufficient colour contrast</li></ul>
        <p><a href="https://www.w3.org/TR/WCAG22/#contrast-minimum" target="_blank" rel="noopener">1.4.3 &ndash; Contrast (Minimum)</a></p>

        <ul><li>There are instances of text as image</li></ul>
        <p><a href="https://www.w3.org/TR/WCAG22/#images-of-text" target="_blank" rel="noopener">1.4.5 &ndash; Images of Text</a></p>

        <ul><li>Content cannot be presented without loss of information or functionality, and requires scrolling in two dimensions</li></ul>
        <p><a href="https://www.w3.org/TR/WCAG22/#reflow" target="_blank" rel="noopener">1.4.10 &ndash; Reflow</a></p>

        <ul><li>Website lacks a &ldquo;skip to main content&rdquo; option</li></ul>
        <p><a href="https://www.w3.org/TR/WCAG22/#bypass-blocks" target="_blank" rel="noopener">2.4.1 &ndash; Bypass Blocks</a></p>

        <ul><li>Some links lack meaningful hypertext regarding target location</li></ul>
        <p><a href="https://www.w3.org/TR/WCAG22/#link-purpose-in-context" target="_blank" rel="noopener">2.4.4 &ndash; Link Purpose (In Context)</a></p>

        <ul><li>Inadequate focus indicator visibility hinders ease of keyboard operability</li></ul>
        <p><a href="https://www.w3.org/TR/WCAG22/#focus-visible" target="_blank" rel="noopener">2.4.7 &ndash; Focus Visible</a></p>

        <ul><li>Some actions do not alert the user before opening a new tab / window</li></ul>
        <p><a href="https://www.w3.org/TR/WCAG22/#on-input" target="_blank" rel="noopener">3.2.2 &ndash; On Input</a></p>

        <ul><li>Content is not robust enough that it can be interpreted by a wide variety of user agents, including assistive technologies e.g., buttons do not all have discernible text</li></ul>
        <p><a href="https://www.w3.org/TR/WCAG22/#name-role-value" target="_blank" rel="noopener">4.1.2 - Name, Role, Value</a></p>

