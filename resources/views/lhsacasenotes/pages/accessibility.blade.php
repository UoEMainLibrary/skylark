        <p><a href="https://www.ed.ac.uk/student-disability-service/staff/supporting-students/accessible-technology" target="_blank" rel="noopener">Information on SensusAccess</a></p>

        <h2>How accessible this website is</h2>
        <p>We know some parts of this website are not fully accessible:</p>
        <ul>
            <li>Colour contrast does not always meet required standards</li>
            <li>Most images do not have any alternative text</li>
            <li>Magnification beyond 175% leads to loss of content</li>
            <li>Reflow is not operational to 400%</li>
            <li>Some text is presented as an image</li>
            <li>Some links lack underlines for easy visual identification</li>
            <li>Links in the main body lack hypertext about where it leads</li>
            <li>Lack of a &ldquo;skip to main content&rdquo; button</li>
            <li>Users are not alerted when some hyperlinks open new tab / window</li>
            <li>Search button does not have discernible text</li>
            <li>It is not always clear where you have keyboard navigated too</li>
            <li>It is not possible to view the site in landscape and portrait.</li>
            <li>Some information is conveyed by colour only</li>
        </ul>

