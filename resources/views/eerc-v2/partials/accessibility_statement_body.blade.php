<h2>Technical information about this website&rsquo;s accessibility</h2>

<p>The University of Edinburgh is committed to making its websites and applications accessible, in accordance with the Public Sector Bodies (Websites and Mobile Applications) (No. 2) Accessibility Regulations 2018.</p>

<h3>Compliance status</h3>

<p>This website is partially compliant with the <a href="https://www.w3.org/TR/WCAG22/" target="_blank" rel="noopener">Web Content Accessibility Guidelines (WCAG) 2.2 AA standard<span class="sr-only"> (opens in a new tab)</span></a>, due to the non-compliances listed below.</p>

<p>The full guidelines are available at the link above.</p>

<h2>Non accessible content</h2>

<p>The content listed below is non-accessible for the following reasons.</p>

<h3>Noncompliance with the accessibility regulations</h3>

<p>The following items do not comply with the WCAG 2.2 AA success criteria:</p>

<ul>
    <li>Not all non-text items have alternative text — <a href="https://www.w3.org/TR/WCAG22/#non-text-content" target="_blank" rel="noopener">1.1.1 – Non-text Content<span class="sr-only"> (opens in a new tab)</span></a></li>
    <li>Some video content does not have transcript present — <a href="https://www.w3.org/TR/WCAG22/#audio-only-and-video-only-prerecorded" target="_blank" rel="noopener">1.2.1 - Audio-only and Video-only<span class="sr-only"> (opens in a new tab)</span></a></li>
    <li>Some audio content does not have human corrected captions — <a href="https://www.w3.org/TR/WCAG22/#captions-prerecorded" target="_blank" rel="noopener">1.2.2 - Captions<span class="sr-only"> (opens in a new tab)</span></a></li>
    <li>There is no audio description provided for audio-visual content — <a href="https://www.w3.org/TR/WCAG22/#audio-description-prerecorded" target="_blank" rel="noopener">1.2.5 - Audio Description<span class="sr-only"> (opens in a new tab)</span></a></li>
    <li>Some information is conveyed by colour only e.g. links — <a href="https://www.w3.org/TR/WCAG22/#use-of-color" target="_blank" rel="noopener">1.4.1 - Use of Color<span class="sr-only"> (opens in a new tab)</span></a></li>
    <li>There were colour contrast issues — <a href="https://www.w3.org/TR/WCAG22/#contrast-minimum" target="_blank" rel="noopener">1.4.3 - Contrast (Minimum)<span class="sr-only"> (opens in a new tab)</span></a></li>
    <li>Readability of the content is affected when site is magnified beyond 150% — <a href="https://www.w3.org/TR/WCAG22/#resize-text" target="_blank" rel="noopener">1.4.4 – Resize Text<span class="sr-only"> (opens in a new tab)</span></a></li>
    <li>Some items are examples of text as an image and do not contain the appropriate alternative text — <a href="https://www.w3.org/TR/WCAG22/#images-of-text" target="_blank" rel="noopener">1.4.5 – Images of Text<span class="sr-only"> (opens in a new tab)</span></a></li>
    <li>Reflow is not operational up to 400% — <a href="https://www.w3.org/TR/WCAG22/#reflow" target="_blank" rel="noopener">1.4.10 – Reflow<span class="sr-only"> (opens in a new tab)</span></a></li>
    <li>Tooltips are not accessible by keyboard or assistive software — <a href="https://www.w3.org/TR/WCAG22/#content-on-hover-or-focus" target="_blank" rel="noopener">1.4.13 – Content on Hover or Focus<span class="sr-only"> (opens in a new tab)</span></a></li>
    <li>Not all content can be reached by keyboard e.g. maps — <a href="https://www.w3.org/TR/WCAG22/#keyboard" target="_blank" rel="noopener">2.1.1. - Keyboard<span class="sr-only"> (opens in a new tab)</span></a></li>
    <li>Some links do not contain meaningful hypertext to inform the user of their target location — <a href="https://www.w3.org/TR/WCAG22/#link-purpose-in-context" target="_blank" rel="noopener">2.4.4 - Link Purpose in Context<span class="sr-only"> (opens in a new tab)</span></a></li>
    <li>Headings are not formatted correctly and some heading levels are missed, which affects screen reader software — <a href="https://www.w3.org/TR/WCAG22/#headings-and-labels" target="_blank" rel="noopener">2.4.6 - Headings and Labels<span class="sr-only"> (opens in a new tab)</span></a></li>
    <li>The website is not fully compatible with assistive software as not all items are coded correctly e.g. some buttons do not have discernible text and some elements use non supported ARIA attributes — <a href="https://www.w3.org/TR/WCAG22/#name-role-value" target="_blank" rel="noopener">4.1.2 - Name-Role-Value<span class="sr-only"> (opens in a new tab)</span></a></li>
    <li>Not all PDF documents are fully accessible — <a href="https://www.w3.org/TR/WCAG22/#name-role-value" target="_blank" rel="noopener">4.1.2 - Name-Role-Value<span class="sr-only"> (opens in a new tab)</span></a></li>
</ul>

<p>We aim to improve our websites accessibility on a regular and continuous basis. See the section below (&lsquo;What we&rsquo;re doing to improve accessibility&rsquo;) on how we are improving our site accessibility.</p>

