<h2>How accessible this website is</h2>

<p>We know some parts of this website are not fully accessible:</p>

<ul>
    <li>There are some colour contrast issues</li>
    <li>There are examples of text as images</li>
    <li>Some images do not have alternative text</li>
    <li>Not all hyperlinks are formatted correctly with meaningful hypertext</li>
    <li>There is some information conveyed by colour only</li>
    <li>Not all content can be reached by keyboard e.g. maps</li>
    <li>Headings are not properly used, with some levels being skipped</li>
    <li>Site cannot be magnified beyond 150% without content overlap</li>
    <li>Reflow is not operational beyond 125%</li>
    <li>Tooltips cannot be reached by keyboard or assistive software</li>
    <li>Screen readers are not fully compatible with the site</li>
    <li>Not all audio-visual content carries human-correct captions</li>
    <li>Not all audio-visual content has audio description</li>
    <li>Not all audio content has a text alternative</li>
    <li>Voice recognition software is not fully compatible with the site due to formatting issues with the site</li>
    <li>Some files on the site are in PDF format and are not fully accessible</li>
</ul>

